    void CreateMeshDataParallel()
    {
        // Declare and initialize buffers for the compute shader
        ComputeBuffer triangles_buffer = new ComputeBuffer(width * height * width * 5, sizeof(float) * 3 * 3, ComputeBufferType.Append);
        ComputeBuffer triangles_counter_buffer = new ComputeBuffer(1, sizeof(int), ComputeBufferType.Raw);
        ComputeBuffer terrain_buffer = new ComputeBuffer((width + 1) * (height + 1) * (width + 1), sizeof(float));
        int kernel_id = march_shader.FindKernel("MarchingCube");

        // Initialize buffers and variables in the compute shader
        triangles_buffer.SetCounterValue(0);
        terrain_buffer.SetData(terrainMap);
        march_shader.SetBuffer(kernel_id, "triangles", triangles_buffer);
        march_shader.SetBuffer(kernel_id, "terrainMap", terrain_buffer);
        march_shader.SetInt("height", height);
        march_shader.SetInt("width", width);
        march_shader.SetFloat("terrainSurface", terrainSurface);
        int[] start_pos = new int[] { x_start, y_start, z_start };
        march_shader.SetInts("start_pos", start_pos);

        // Launch the kernel
        march_shader.Dispatch(kernel_id, 4, 4, 4);

        // Get the number of triangles returned by the compute shader
        ComputeBuffer.CopyCount(triangles_buffer, triangles_counter_buffer, 0);
        int[] triCountArray = { 0 };
        triangles_counter_buffer.GetData(triCountArray);
        int numTris = triCountArray[0];

        int num_triangles = numTris;
        Triangle[] tris = new Triangle[num_triangles];
        triangles_buffer.GetData(tris, 0, 0, num_triangles);

        // Add the vertices and the triangles to the coresponding lists
        for (int i = 0; i < num_triangles; i++)
        {
            vertices.Add(tris[i].vertex1);
            triangles.Add(vertices.Count - 1);
            vertices.Add(tris[i].vertex2);
            triangles.Add(vertices.Count - 1);
            vertices.Add(tris[i].vertex3);
            triangles.Add(vertices.Count - 1);

        }
    }

    // March through all the cubes (serialized)
    void CreateMeshData()
    {
        for (int x = 0; x < width; x++)
        {
            for (int z = 0; z < width; z++)
            {
                for (int y = 0; y < height; y++)
                {
                    MarchCube(new Vector3Int(x_start + x, y_start + y, z_start + z), triangles);
                }
            }
        }
    }

    // Calculate a 3D Perlin Noise value
    float Perlin3d(float x, float y, float z)
    {
        float xy = Mathf.PerlinNoise(x, y);
        float xz = Mathf.PerlinNoise(x, z);
        float yz = Mathf.PerlinNoise(y, z);

        float yx = Mathf.PerlinNoise(y, x);
        float zx = Mathf.PerlinNoise(z, x);
        float zy = Mathf.PerlinNoise(z, y);

        float xyz = xy + xz + yz + yx + zx + zy;

        return xyz / 6f;

    }


    // Use Perlin Noise to get the values that will be used to create the mesh with the marching cube algorithm.
    void PopulateTerrainMap()
    {
        for (int x = 0; x < width + 1; x++)
        {
            for (int z = 0; z < width + 1; z++)
            {
                for (int y = 0; y < height + 1; y++)
                {

                    if (perlinType == 2)
                    {

                        terrainSurface = 0.4f;
                        float thisHeight = Perlin3d((float)(x + x_start) * noiseScale, (float)(y + y_start) * noiseScale, (float)(z + z_start) * noiseScale);
                        terrainMap[x * ((width + 1) * (height + 1)) + y * (height + 1) + z] = thisHeight;
                        
                    } else if (perlinType == 1)
                    {

                        y_start = -5;
                        terrainSurface = 5f;
                        float thisHeight = (float)height * Mathf.PerlinNoise((float)(x_start + x) / 16f * 0.5f + 0.001f, (float)(z_start + z) / 16f * 0.5f + 0.001f);
                        terrainMap[x * ((width + 1) * (height + 1)) + y * (height + 1) + z] = (float)(y) - thisHeight;
                    
                    } else if (perlinType == 0)
                    {

                        terrainSurface = 0.5f;
                        float thisHeight = (float)height * Mathf.PerlinNoise((float)(x_start + x) / 16f * 1.5f + 0.001f, (float)(z_start + z) / 16f * 1.5f + 0.001f);
                        terrainMap[x * ((width + 1) * (height + 1)) + y * (height + 1) + z] = (float)(y_start + y) - thisHeight;
                    
                    }
                    
                }
            }
        }
    }

    int GetCubeConfiguration(float[] cube)
    {
        int configurationIndex = 0;

        for (int i = 0; i < 8; i++)
        {
            if (cube[i] > terrainSurface)
            {
                configurationIndex |= 1 << i;
            }
        }

        return configurationIndex;
    }

    void MarchCube(Vector3Int position, List<int> triangles)
    {
        // Get the cube values at the specified position
        float[] cube = new float[8];
        for (int i = 0; i < 8; i++)
        {
            Vector3Int corner = position + cornerTable[i] - new Vector3Int(x_start, y_start, z_start);
            cube[i] = SampleTerrain(corner);
        }

        // Get the cube's configuration
        int config_index = GetCubeConfiguration(cube);

        // The configurations with all corners inside or outside of the shape are irrelevant
        if (config_index == 0 || config_index == 255)
        {
            return;
        }

        // Iterate through all the edges t
        int edgeIndex = 0;
        for (int t = 0; t < 5; t++)
        {
            for (int p = 0; p < 3; p++)
            {
                int ind = triangleTable[config_index, edgeIndex];

                if (ind == -1)
                {
                    return;
                }

                Vector3 vert1 = position + cornerTable[edgeTable[ind, 0]];
                Vector3 vert2 = position + cornerTable[edgeTable[ind, 1]];

                Vector3 vertPosition = (vert1 + vert2) / 2f;
                if (smoothTerrain)
                {
                    float vert1Sample = cube[edgeTable[ind, 0]];
                    float vert2Sample = cube[edgeTable[ind, 1]];

                    float difference = vert2Sample - vert1Sample;
                    float percentage = 0;

                    if (difference != 0)
                    {
                        percentage = (terrainSurface - vert1Sample) / difference; 
                    }

                    vertPosition = vert1 + ((vert2 - vert1) * percentage);
                }

                vertices.Add(vertPosition);
                triangles.Add(vertices.Count - 1);

                edgeIndex++;

            }
        }
    }

    // Get the value at a certain point for a terrain patch
    public float SampleTerrain(Vector3Int point)
    {
        return terrainMap[point.x * (height + 1) * (width + 1) + point.y * (height + 1) + point.z];
    }


    // Clear the vertices and triangles list's data
    void ClearMeshData()
    {
        vertices.Clear();
        triangles.Clear();
    }

    // Build the terrain patch
    void BuildMesh()
    {

        // Create a new mesh based on the vertices and triangles lists
        Mesh mesh = new Mesh();
        mesh.vertices = vertices.ToArray();
        mesh.triangles = triangles.ToArray();
        mesh.RecalculateNormals();
        meshFilter.mesh = mesh;

        //Add a mesh collider if it doesn't exist
        if (gameObject.GetComponent<MeshCollider>() == null)
        {
            this.gameObject.AddComponent<MeshCollider>();
        }

        //Add the generated mesh to the mesh collider
        MeshCollider col = this.gameObject.GetComponent<MeshCollider>();
        col.sharedMesh = meshFilter.mesh;
    }

    // Modify the terrainMap in such a way that a hole will be created in
    // the terrain at a certain position.
    void CreateHoleInTerrain(Vector3 position)
    {
        Vector3Int offset = new Vector3Int(x_start, y_start, z_start);
        Vector3Int point = (Vector3Int.FloorToInt(position) - offset);

        for (int i = -1; i <= 1; i++)
        {
            for (int j = -1; j <= 1; j++)
            {
                for (int k = -1; k <= 1; k++)
                {

                    if (point.x + i > width || point.y + j > height || point.z + k > width ||
                        point.x + i < 0 || point.y + j < 0 || point.z + k < 0)
                        continue;

                    terrainMap[(point.x + i) * (height + 1) * (width + 1) + (point.y + j) * (height + 1) + point.z + k] = terrainSurface + 1;

                }
            }
        }
    }

    private void OnCollisionEnter(Collision collision)
    {

        // Checks if a projectile hit the terrain
        if (collision.gameObject.tag == "Projectile")
        {
            // Instantiate the Explsion particle filter
            GameObject newExplosion = Instantiate(explosion, collision.GetContact(0).point, Quaternion.identity);
            Destroy(newExplosion, 2);

            // Create a hole in the terrain at the point of impact with the projectile and recalculate the mesh
            CreateHoleInTerrain(collision.GetContact(0).point);
            ClearMeshData();
            CreateMeshDataParallel();
            BuildMesh();
        }
    }

